function getRandomPYQs(chapter, count = 10) {
  const questions = pyqBank[chapter] || [];
  if (questions.length === 0) {
    return [{
      year: 0,
      exam: 'Expected Question',
      paper: 'GS Paper 1',
      question: `What is the historical significance of ${chapter}?`,
      options: ['A) It was a major political event', 'B) It influenced cultural development', 'C) It shaped economic systems', 'D) All of the above'],
      correctAnswer: 'D) All of the above',
      explanation: `${chapter} is an important chapter in NCERT history curriculum that covers significant historical events, cultural developments, and economic systems relevant to UPSC preparation.`,
      difficulty: 'medium',
      topic: chapter
    }];
  }
  
  const shuffled = [...questions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}

